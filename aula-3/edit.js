function add(first, second){
    return first + second;
}

function sub(first, second){
    return first - second;
}

function mul(first, second){
    return first*second;
}

function div(first, second){
    return first/second;
}

function is_even(number){
    if (number%2 == 0){
        return true;
    }
    else{
        return false;
    }
}


